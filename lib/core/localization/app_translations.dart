class LanguageProvider extends ChangeNotifier {
  static const String _languageKey = 'app_language';
  String _currentLocale = 'en';
  bool _initialized = false;

  String get currentLocale => _currentLocale;
  bool get isInitialized => _initialized;

  /// Inicializa el idioma: carga de SharedPreferences o usa el del sistema
  Future<void> initialize() async {
    if (_initialized) return;

    try {
      final prefs = await SharedPreferences.getInstance();
      final savedLanguage = prefs.getString(_languageKey);

      if (savedLanguage != null && ['en', 'es', 'ko'].contains(savedLanguage)) {
        // Usar idioma guardado
        _currentLocale = savedLanguage;
      } else {
        // Primera vez: detectar idioma del sistema
        _currentLocale = AppTranslations.getSystemLanguage();
        // Guardar para futuras sesiones
        await prefs.setString(_languageKey, _currentLocale);
      }
    } catch (e) {
      // Si hay error, usar idioma del sistema
      _currentLocale = AppTranslations.getSystemLanguage();
    }

    _initialized = true;
    notifyListeners();
  }

  /// Cambia el idioma y lo guarda en SharedPreferences
  Future<void> setLocale(String locale) async {
    if (!['en', 'es', 'ko'].contains(locale)) return;

    _currentLocale = locale;
    notifyListeners();

    // Guardar preferencia
    try {
      final prefs = await SharedPreferences.getInstance();
      await prefs.setString(_languageKey, locale);
    } catch (e) {
      // Ignorar errores de guardado
    }
  }

  String tr(String key) {
    return AppTranslations.translate(key, _currentLocale);
  }
}
